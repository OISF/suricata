#include <stdint.h>

/**
 * \brief Fix-up an IP checksum.
 *
 * \param sum The current checksum.
 * \param old Value of old header parameter.
 * \param new Value of new header parameter.
 *
 * \retval New checksum.
 */
uint16_t
FixChecksum(uint16_t sum, uint16_t old, uint16_t new)
{
    uint32_t l;

    l = sum + old - new;
    l = (l >> 16) + (l & 65535);
    l = l & 65535;

    return l;
}
